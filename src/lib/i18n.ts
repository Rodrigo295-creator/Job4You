const DICTS: Record<AppLocale, Record<string, string>> = {
  'pt-BR': PT_BR,
  'en-US': EN_US,
  'es-ES': ES_ES,
};

function resolveDict(locale: AppLocale): Record<string, string> {
  return DICTS[locale] ?? PT_BR;
}

export function translate(
  key: string,
  params?: TranslateParams,
  locale: AppLocale = 'pt-BR',
): string {
  const dict = resolveDict(locale);
  const fallback = PT_BR;
  let text = dict[key] ?? fallback[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replaceAll(`{${k}}`, String(v));
    }
  }
  return text;
}

export function readStoredLocale(): AppLocale {
  try {
    const stored = localStorage.getItem(LOCALE_STORAGE_KEY);
    if (stored === 'pt-BR' || stored === 'en-US' || stored === 'es-ES') {
      return stored;
    }
  } catch {
    /* ignore */
  }
  return 'pt-BR';
}
